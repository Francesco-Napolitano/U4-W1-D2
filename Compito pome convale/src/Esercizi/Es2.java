package Esercizi;

public class Es2 {
    public static String stampaValore (int numero){
        switch (numero){
            case 0:
                return "zero";
            case 1:
                return "one";
            case 2:
                return "two";
            case 3:
                return "three";
            default:
                return "Error sei un fallito";
        }

    }
}
